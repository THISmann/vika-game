// Langue actuelle (stock√©e dans localStorage)
const currentLanguage = ref(localStorage.getItem('gameLanguage') || 'fr')

// Fonction pour changer la langue
export function setLanguage(lang) {
  if (translations[lang]) {
    currentLanguage.value = lang
    localStorage.setItem('gameLanguage', lang)
  }
}

// Fonction pour obtenir la traduction
export function t(key) {
  const lang = currentLanguage.value
  return translations[lang]?.[key] || translations.fr[key] || key
}

// Composable Vue
export function useI18n() {
  const language = computed(() => currentLanguage.value)
  
  const changeLanguage = (lang) => {
    setLanguage(lang)
  }
  
  // Wrapper r√©actif pour la fonction t
  const translate = (key) => {
    // Acc√©der √† currentLanguage.value pour rendre la fonction r√©active
    const lang = currentLanguage.value
    return translations[lang]?.[key] || translations.fr[key] || key
  }
  
  return {
    language,
    changeLanguage,
    t: translate,
    translate,
    availableLanguages: ['fr', 'en', 'ru', 'no']
  }
}

